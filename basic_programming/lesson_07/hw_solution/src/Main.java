public class Main {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        int result = sum(num1, num2);
        System.out.println("Сумма чисел " + num1 + " и " + num2 + " равна " + result);
    }
    public static int sum(int a, int b) {
        return a + b;
    }
}
